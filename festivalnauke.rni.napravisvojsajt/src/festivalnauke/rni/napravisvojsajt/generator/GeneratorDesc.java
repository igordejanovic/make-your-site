package festivalnauke.rni.napravisvojsajt.generator;

public class GeneratorDesc {

	public String id;
	public String name;
	
	public GeneratorDesc(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}

	
}
